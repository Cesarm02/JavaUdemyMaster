package basico;

public class Clase3_Operadores {

	public static void main(String[] args) {

		//Operadores Aritmeticos - Asignación
		int i = 5, j = 4;
		
		int suma = i + j;
		System.out.println("suma = " + suma);
		
		int resta = i - j;
		System.out.println("resta = " + resta);
		
		int mul = i * j;
		System.out.println("multiplicacion = " + mul);
		
		
	}

}
